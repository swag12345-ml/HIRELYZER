def slugify(text: str) -> str:
    """Convert text into a safe slug (lowercase, hyphenated, no special chars)."""
    text = text.lower().strip()
    text = re.sub(r"[^\w\s-]", "", text)
    text = re.sub(r"\s+", "-", text)
    return text

